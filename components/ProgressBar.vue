<template>
  <div class="progress-block">
    <div class="counter">{{ is小omplitedBar ? complitedCounter : notComplitedCounter }} tasks</div>
    <div class="name">{{ name }}</div>
    <b-progress 
      :value="is小omplitedBar ? complitedCounter : notComplitedCounter" 
      :max="allTodos.length"
      height="4px"
      class="progress-block-bar" 
      :class="is小omplitedBar ? 'complited' : 'not-complited'"
    />
  </div>
</template>

<script>
import { mapGetters } from 'vuex';
import { BProgress } from 'bootstrap-vue'

export default {
  name: 'ProgressBar',
  props: {
    name: String,
    is小omplitedBar: Boolean,
  },
  components: { BProgress },
  computed: {
    ...mapGetters(['allTodos']),
    complitedCounter() {
      return this.allTodos.filter(todo => todo.checked).length;
    },
    notComplitedCounter() {
      return this.allTodos.filter(todo => !todo.checked).length;
    },
  },
}
</script>
